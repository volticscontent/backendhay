// GET /services
router.get('/services', async (_req: Request, res: Response) => {
  try {
    await ensureServicesTable();
    const { nameCol, valueCol, descriptionCol } = await getServiceColumns();
    await seedIfEmpty(nameCol, valueCol, descriptionCol);
    const result = await pool.query(
      `SELECT id, ${nameCol} AS name, ${valueCol} AS value, ${descriptionCol} AS description, created_at, updated_at FROM services ORDER BY id ASC`,
    );
    res.json(result.rows);
  } catch (err) {
    console.error('Erro ao listar serviços:', err);
    res.status(500).json({ error: 'Erro ao listar serviços' });
  }
});

// POST /services
router.post('/services', async (req: Request, res: Response) => {
  try {
    await ensureServicesTable();
    const { nameCol, valueCol, descriptionCol } = await getServiceColumns();
    await seedIfEmpty(nameCol, valueCol, descriptionCol);
    const { name, value, description } = req.body;
    if (!name || value === undefined) return void res.status(400).json({ error: 'Nome e valor são obrigatórios' });
    const result = await pool.query(
      `INSERT INTO services (${nameCol},${valueCol},${descriptionCol}) VALUES ($1,$2,$3)
       RETURNING id, ${nameCol} AS name, ${valueCol} AS value, ${descriptionCol} AS description, created_at, updated_at`,
      [name, value, description || ''],
    );
    res.json(result.rows[0]);
  } catch (err) {
    console.error('Erro ao criar serviço:', err);
    res.status(500).json({ error: 'Erro ao criar serviço' });
  }
});

// PUT /services/:id
router.put('/services/:id', async (req: Request, res: Response) => {
  try {
    await ensureServicesTable();
    const { nameCol, valueCol, descriptionCol } = await getServiceColumns();
    const { id } = req.params;
    const { name, value, description } = req.body;
    const setParts: string[] = [];
    const values: unknown[] = [];
    let idx = 1;
    if (typeof name === 'string' && name.length > 0) { setParts.push(`${nameCol} = $${idx++}`); values.push(name); }
    if (value !== undefined && !Number.isNaN(Number(value))) { setParts.push(`${valueCol} = $${idx++}`); values.push(Number(value)); }
    if (typeof description === 'string') { setParts.push(`${descriptionCol} = $${idx++}`); values.push(description); }
    setParts.push('updated_at = NOW()');
    values.push(id);
    const sql = `UPDATE services SET ${setParts.join(', ')} WHERE id = $${idx} RETURNING id, ${nameCol} AS name, ${valueCol} AS value, ${descriptionCol} AS description, created_at, updated_at`;
    const result = await pool.query(sql, values);
    if (result.rowCount === 0) return void res.status(404).json({ error: 'Serviço não encontrado' });
    res.json(result.rows[0]);
  } catch (err) {
    console.error('Erro ao atualizar serviço:', err);
    res.status(500).json({ error: 'Erro ao atualizar serviço' });
  }
});

// DELETE /services/:id
router.delete('/services/:id', async (req: Request, res: Response) => {
  try {
    await ensureServicesTable();
    const result = await pool.query('DELETE FROM services WHERE id = $1', [req.params.id]);
    if (result.rowCount === 0) return void res.status(404).json({ error: 'Serviço não encontrado' });
    res.json({ success: true });
  } catch (err) {
    console.error('Erro ao excluir serviço:', err);
    res.status(500).json({ error: 'Erro ao excluir serviço' });
  }
});

export default router;
