import { Router, Request, Response } from 'express';
import pool from '../lib/db';

const router = Router();

// ---- Helpers ----

async function ensureServicesTable() {
  const client = await pool.connect();
  try {
    const r = await client.query(
      `SELECT EXISTS (SELECT 1 FROM information_schema.tables WHERE table_schema='public' AND table_name='services') AS exists`,
    );
    if (!r.rows[0].exists) {
      await client.query(`
        CREATE TABLE services (
          id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
          name TEXT NOT NULL, value NUMERIC(10,2) NOT NULL, description TEXT,
          created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW() NOT NULL,
          updated_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW() NOT NULL
        )
      `);
      return;
    }
    await client.query(`ALTER TABLE services ADD COLUMN IF NOT EXISTS name TEXT`);
    await client.query(`ALTER TABLE services ADD COLUMN IF NOT EXISTS value NUMERIC(10,2)`);
    await client.query(`ALTER TABLE services ADD COLUMN IF NOT EXISTS description TEXT`);
    await client.query(`ALTER TABLE services ADD COLUMN IF NOT EXISTS created_at TIMESTAMP WITHOUT TIME ZONE`);
    await client.query(`ALTER TABLE services ADD COLUMN IF NOT EXISTS updated_at TIMESTAMP WITHOUT TIME ZONE`);
    await client.query(`ALTER TABLE services ALTER COLUMN created_at SET DEFAULT NOW()`);
    await client.query(`ALTER TABLE services ALTER COLUMN updated_at SET DEFAULT NOW()`);
  } finally {
    client.release();
  }
}

async function getServiceColumns() {
  const client = await pool.connect();
  try {
    const res = await client.query(
      `SELECT column_name FROM information_schema.columns WHERE table_schema='public' AND table_name='services'`,
    );
    const cols = res.rows.map((r) => String(r.column_name));
    return {
      nameCol: cols.includes('nome') ? 'nome' : 'name',
      valueCol: cols.includes('valor') ? 'valor' : 'value',
      descriptionCol: cols.includes('descricao') ? 'descricao' : 'description',
    };
  } finally {
    client.release();
  }
}

